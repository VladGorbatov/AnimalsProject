package org.example;

class Animal {
    public String name;
    public String type;
    public int age;
    public double weight;
    public String country;
    public String color;
    public String zooName;

    public String toString() {
        return "Animal [name=" + name + ", type=" + type + ", age=" + age + ", weight=" + weight + ", country="+country+ ", color="+color + ", zooName="+zooName+"]";
    }
}